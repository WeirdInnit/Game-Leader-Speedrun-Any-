def main():
    c = str(input("Happy, sad or surprised? "))
    lowered = c.lower()
    if lowered == "happy":
        happy()
    elif lowered == "sad":
        sad()
    elif lowered == "surprised":
        surprised()
    else:
        print("Invalid")


